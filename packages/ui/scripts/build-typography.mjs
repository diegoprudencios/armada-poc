// ABOUTME: Generates typography.css from semantic.typography composites in armada-tokens.json.
// ABOUTME: Emits --semantic-typography-* CSS variables and .armada-text-* utility classes.

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const tokensPath = join(root, 'src/tokens/armada-tokens.json')
const outPath = join(root, 'src/styles/typography.css')
const variantsPath = join(root, 'src/typography/variants.ts')

const tokens = JSON.parse(readFileSync(tokensPath, 'utf8'))
const { primitives, semantic } = tokens

const CSS_PROP = {
  fontFamily: 'font-family',
  fontWeight: 'font-weight',
  fontSize: 'font-size',
  lineHeight: 'line-height',
  letterSpacing: 'letter-spacing',
}

function getPrimitive(path) {
  const [category, key] = path.split('.')
  const node = primitives[category]?.[key]
  if (!node) throw new Error(`Unknown primitive reference: ${path}`)
  return node.value
}

function primitiveVar(category, key) {
  if (category === 'fontSize') {
    return `calc(var(--primitives-fontSize-${key}) * 1px)`
  }
  if (category === 'fontWeight') {
    return `var(--primitives-fontWeight-${key})`
  }
  if (category === 'fontFamily') {
    return `var(--primitives-fontFamily-${key})`
  }
  if (category === 'lineHeight') {
    return `var(--primitives-lineHeight-${key})`
  }
  if (category === 'letterSpacing') {
    return `var(--primitives-letterSpacing-${key})`
  }
  if (category === 'spacing') {
    return `var(--primitives-spacing-${key})`
  }
  throw new Error(`Unsupported primitive category for CSS var: ${category}`)
}

function resolveValue(raw) {
  if (typeof raw !== 'string') return String(raw)

  const ref = raw.match(/^\{(.+)\}$/)
  if (ref) {
    return primitiveVar(...ref[1].split('.'))
  }

  if (/^\d+$/.test(raw)) {
    return `${raw}px`
  }

  return raw
}

function slug(group, name) {
  return `${group}-${name}`
}

function varName(group, name, prop) {
  const kebab = prop.replace(/([A-Z])/g, '-$1').toLowerCase()
  return `--semantic-typography-${slug(group, name)}-${kebab}`
}

function className(group, name) {
  return `.armada-text-${slug(group, name)}`
}

const lines = [
  '/* AUTO-GENERATED by scripts/build-typography.mjs — do not edit. */',
  '/* Regenerate: npm run tokens:typography --workspace=@armada/ui */',
  '',
  ':root {',
]

const classBlocks = []
const variantSlugs = []

for (const [group, composites] of Object.entries(semantic.typography)) {
  for (const [name, token] of Object.entries(composites)) {
    if (token.type !== 'typography') continue

    const id = slug(group, name)
    variantSlugs.push(id)
    const props = token.value
    const classProps = []

    for (const [prop, raw] of Object.entries(props)) {
      if (prop === 'textCase') continue
      const cssProp = CSS_PROP[prop]
      if (!cssProp) continue

      const resolved = resolveValue(raw)
      lines.push(`  ${varName(group, name, prop)}: ${resolved};`)
      classProps.push(`  ${cssProp}: var(${varName(group, name, prop)});`)
    }

    classBlocks.push(
      `${className(group, name)} {`,
      ...classProps,
      '}',
      '',
    )
  }
}

lines.push('}', '', ...classBlocks)

writeFileSync(outPath, `${lines.join('\n')}\n`)
console.log(`Wrote ${outPath}`)

const variantUnion = variantSlugs.map((id) => `'${id}'`).join(' | ')
const variantsTs = [
  '/* AUTO-GENERATED by scripts/build-typography.mjs — do not edit. */',
  '',
  `export type TypographyVariant = ${variantUnion}`,
  '',
  `export const TYPOGRAPHY_VARIANTS = [${variantSlugs.map((id) => `'${id}'`).join(', ')}] as const satisfies readonly TypographyVariant[]`,
  '',
  'export function typographyClassName(variant: TypographyVariant): string {',
  "  return `armada-text-${variant}`",
  '}',
  '',
].join('\n')

mkdirSync(dirname(variantsPath), { recursive: true })
writeFileSync(variantsPath, variantsTs)
console.log(`Wrote ${variantsPath}`)
